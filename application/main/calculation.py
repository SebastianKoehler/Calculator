def addition(summand1, summand2):
    summe = summand1 + summand2
    return summe


def subtraktion(minuend, subtrahend):
    differenz = minuend - subtrahend
    return differenz


def multiplikation(faktor1, faktor2):
    produkt = faktor1 * faktor2
    return produkt


def division(dividend, divisor):
    quotient = dividend / divisor
    return quotient


def modulo(wert, divisor):
    rest = wert % divisor
    return rest
